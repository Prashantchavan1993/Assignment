package Hundrendtotwohundrend;

public class forandifelse {
    public static void main(String[]args) {


        for (int i = 100; i<=200; i++){

            if (i % 2 == 0) {

                System.out.println(i + "even no.");
            }

            else {

                System.out.println(i + "odd no.");
        }
        }
    }
}
